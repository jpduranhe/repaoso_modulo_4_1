package repaso_modulo_4_1;

public class EjercicioIteracion {

	public static void main(String[] args) {
		int num1=10;
		int num2=10;
		System.out.println("WHILE---------");
		while(num1<10) {
			
			System.out.println(num1);
			num1++;
		}
		
		
		System.out.println("DO WHILE---------");
		do{
			
			System.out.println(num2);
			num2++;
		}while(num2<10);


	}
	
	
	

}
